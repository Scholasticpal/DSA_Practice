// Comparison operators
// == - equal to
// === - strict equal to
// != - not equal to
// !== - strict not equal to
// > - greater than
// < - less than
// >= - greater than or equal to
// <= - less than or equal to

let c = 5;
let d = "5";

// IMPORTANT
console.log("c == d", c==d);
console.log("c != d", c!=d);
console.log("c === d", c===d);
console.log("c !== d", c!==d);




