// Arithematic operators
// + - addition
// - - subtraction
// * - multiplication
// / - division
// % - modulus
// ** - exponentiation
// ++ - increment
// -- - decrement

// Assignment operators
// = - assignment
// += - addition assignment
// -= - subtraction assignment
// *= - multiplication assignment
// /= - division assignment
// %= - modulus assignment
// **= - exponentiation assignment

let a = 10
let b = 2
console.log("a + b = " + (a+b));
console.log("a - b = " + (a-b));
console.log("a * b = " + (a*b));
console.log("a / b = " + (a/b));
console.log("a % b = " + (a%b));
console.log("a ** b = " + (a**b));

console.log("++a = " + (++a));  //-> 11
console.log("a++ = " + (a++)); //-> 11
console.log("--a = " + (--a)); //-> 11
console.log("a-- = " + (a--)); //-> 11
console.log("a = " + a);    // -> 10

