/*LINKED LIST

->  The idea is to drop contiguous memory requirements so that insertions  and
    deletions can efficiently happen at the middle as well.

->  And no need to pre-allocate the space (no extra nodes)

-> In a Linked List:
    - Insertion and Deletion at random positions possible.
    - No predefined size needed.

-> To understand its memory storage, consider memory as an array of bytes
   and we can address the bytes*/
